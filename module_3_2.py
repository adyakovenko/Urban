default_sender = 'university.help@gmail.com'


def send_email(message, recipient, sender=default_sender):
    if not is_possible_address(sender) or not is_possible_address(recipient):
        print(f'Невозможно отправить письмо с адреса {sender} на адрес {recipient}')
    elif sender == recipient:
        print('Нельзя отправить письмо самому себе!')
    elif sender == default_sender:
        print(f'Письмо успешно отправлено с адреса {sender} на адрес {recipient}.')
    else:
        print(f'НЕСТАНДАРТНЫЙ ОТПРАВИТЕЛЬ! Письмо отправлено с адреса {sender} на адрес {recipient}')


def is_possible_address(address):
    return address.endswith(('.com', '.ru', '.net')) and r'@' in address


